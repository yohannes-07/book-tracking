export class RandomValues {
    randomValues: {
      name: string;
    };
  
    constructor() {
      this.randomValues = {
        name: "",
      };
      this.randomValues.name = this.generateRandomString(10);
    }
  
    generateRandomString(length: number): string {
      const characters = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
      let result = '';
      for (let i = 0; i < length; i++) {
        result += characters.charAt(Math.floor(Math.random() * characters.length));
      }
      return result;
    }
  }
  
  declare global {
    namespace Cypress {
      interface Chainable {
        randomBookTitle(): Cypress.Chainable<string>;
      }
    }
  }
  
  Cypress.Commands.add('randomBookTitle', () => {
    const randomValues = new RandomValues();
    return cy.wrap(randomValues.randomValues.name);
  });
  